#Area of a circle
radius = float(input('what is the radius of the circle? >>>'))
pi = 3.14159
area = pi * radius**2
print('you entered' ,radius, 'the area of the circle is' ,area) #Print function

#Temperature conversion
far = float(input('What is the temperature in Farenheit? '))
